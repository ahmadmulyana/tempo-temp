                	<p>TEMPO.CO, Jakarta - Pemerintah tidak terlalu mempermasalahkan rencana produsen mobil listrik asal Amerika Serikat, Tesla Inc. yang akan membangun pabrik di India. Staf Khusus Menteri BUMN Erick Thohir, Arya Sinulingga, mengatakan bahwa rencana investasi perusahaan milik Elon Musk di India itu adalah untuk membangun pabrik mobil listrik.</p>

<p>Sementara, pendekatan Pemerintah Indonesia dengan Tesla selama ini bukan untuk rencana investasi pabrik mobil. Penjajakan yang dilakukan dengan Tesla, menurut Arya, terkait investasi di bidang baterai kendaraan listrik atau sistem penyimpanan energi (electric storage system/ESS).</p>

<p>"Jadi, ketika kemarin dikatakan Tesla itu ke India ya, kita enggak merasa kecolongan. Karena kita bukan ingin membangun pabrik mobil listrik gitu," ujar Arya dalam acara Prospek Pembentukan Holding Baterai, Kamis, 4 Maret 2021.</p>
